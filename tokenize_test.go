func doTests(t *testing.T, prefix string, config Config, cases ...[]Tokens) {
	for _, tcl := range cases {
		for i, tc := range tcl {
			tc := tc
			desc := "null"
			if len(tc) > 0 {
				desc = tc[0].Text
			}
			t.Run(fmt.Sprintf("%s%03d_%s", prefix, i+1, desc), func(t *testing.T) {
				text := tc.String()
				t.Log("---------------------------------------")
				t.Log(text)
				t.Log("-----------------")
				got := Tokenize(text, config)
				stripDebug := func(tokens Tokens) Tokens {
					n := make([]Token, len(tokens))
					copy(n, tokens)
					for i := range n {
						n[i].SetDebug("")
					}
					return n
				}
				if !assert.Equal(t, text, got.String(), tc.String()) || !assert.Equal(t, tc, stripDebug(got), tc.String()) {
					dumpTokens(t, "want", tc)
					dumpTokens(t, "got", got)
				}
			})
		}
	}
}

